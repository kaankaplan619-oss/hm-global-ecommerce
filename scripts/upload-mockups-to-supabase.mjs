import { createClient } from "@supabase/supabase-js";
import { promises as fs } from "node:fs";
import path from "node:path";

const ROOT_DIR = process.cwd();
const MOCKUPS_DIR = path.join(ROOT_DIR, "public", "mockups");
const OUTPUT_FILE = path.join(ROOT_DIR, "lib", "mockup-urls.ts");
const ENV_FILE = path.join(ROOT_DIR, ".env.local");
const BUCKET_NAME = "mockups";

const args = new Set(process.argv.slice(2));
const isExecute = args.has("--execute");
const isDryRun = !isExecute || args.has("--dry-run");

function parseEnv(content) {
  const env = {};

  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const equalsIndex = line.indexOf("=");
    if (equalsIndex === -1) continue;

    const key = line.slice(0, equalsIndex).trim();
    let value = line.slice(equalsIndex + 1).trim();

    if (
      (value.startsWith("\"") && value.endsWith("\"")) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    env[key] = value;
  }

  return env;
}

async function loadEnv() {
  let localEnv = {};

  try {
    localEnv = parseEnv(await fs.readFile(ENV_FILE, "utf8"));
  } catch (error) {
    if (error?.code !== "ENOENT") throw error;
  }

  return {
    supabaseUrl: process.env.NEXT_PUBLIC_SUPABASE_URL ?? localEnv.NEXT_PUBLIC_SUPABASE_URL,
    serviceRoleKey: process.env.SUPABASE_SERVICE_ROLE_KEY ?? localEnv.SUPABASE_SERVICE_ROLE_KEY,
  };
}

async function walkPngFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...await walkPngFiles(fullPath));
      continue;
    }

    if (entry.isFile() && entry.name.toLowerCase().endsWith(".png")) {
      files.push(fullPath);
    }
  }

  return files.sort((a, b) => a.localeCompare(b));
}

function toStoragePath(filePath) {
  return path.relative(MOCKUPS_DIR, filePath).split(path.sep).join("/");
}

function parseMockupPath(storagePath) {
  const parsed = path.posix.parse(storagePath);
  const productKey = parsed.dir || "root";
  const directViewMatch = parsed.name.match(/^(front|back|detail)$/);
  const colorViewMatch = parsed.name.match(/^(.+)-(front|back|detail(?:-.+)?)$/);

  if (colorViewMatch) {
    return {
      productKey,
      colorKey: colorViewMatch[1],
      viewKey: colorViewMatch[2],
    };
  }

  if (directViewMatch) {
    return {
      productKey,
      colorKey: "default",
      viewKey: directViewMatch[1],
    };
  }

  return {
    productKey,
    colorKey: "default",
    viewKey: parsed.name,
  };
}

function setNestedUrl(urls, storagePath, publicUrl) {
  const { productKey, colorKey, viewKey } = parseMockupPath(storagePath);

  urls[productKey] ??= {};
  urls[productKey][colorKey] ??= {};
  urls[productKey][colorKey][viewKey] = publicUrl;
}

function buildMockupUrlsFile(urls) {
  return [
    "/* Auto-generated by scripts/upload-mockups-to-supabase.mjs. */",
    "/* Do not edit manually. */",
    "",
    "export const MOCKUP_URLS = ",
    `${JSON.stringify(urls, null, 2)} as const;`,
    "",
    "export type MockupUrls = typeof MOCKUP_URLS;",
    "",
  ].join("\n");
}

async function ensureBucket(supabase) {
  const { data: buckets, error: listError } = await supabase.storage.listBuckets();
  if (listError) throw new Error(`Cannot list Supabase buckets: ${listError.message}`);

  if (buckets.some((bucket) => bucket.name === BUCKET_NAME)) {
    return;
  }

  const { error: createError } = await supabase.storage.createBucket(BUCKET_NAME, {
    public: true,
    fileSizeLimit: null,
    allowedMimeTypes: ["image/png"],
  });

  if (createError) throw new Error(`Cannot create "${BUCKET_NAME}" bucket: ${createError.message}`);
}

async function uploadFile(supabase, filePath, storagePath) {
  const body = await fs.readFile(filePath);
  const { error } = await supabase.storage.from(BUCKET_NAME).upload(storagePath, body, {
    contentType: "image/png",
    upsert: true,
  });

  if (error) throw new Error(`Upload failed for ${storagePath}: ${error.message}`);

  const { data } = supabase.storage.from(BUCKET_NAME).getPublicUrl(storagePath);
  return data.publicUrl;
}

async function main() {
  const files = await walkPngFiles(MOCKUPS_DIR);
  const storagePaths = files.map(toStoragePath);

  console.log(`HM Global mockups Supabase uploader`);
  console.log(`Mode: ${isDryRun ? "dry-run" : "execute"}`);
  console.log(`PNG found: ${files.length}`);
  console.log(`Bucket: ${BUCKET_NAME}`);
  console.log(`Output: ${path.relative(ROOT_DIR, OUTPUT_FILE)}`);

  if (isDryRun) {
    console.log("\nNo bucket creation, upload, or file generation will run in dry-run mode.");
    console.log("\nFirst planned uploads:");
    for (const storagePath of storagePaths.slice(0, 20)) {
      const parsed = parseMockupPath(storagePath);
      console.log(`- ${storagePath} -> ${parsed.productKey} / ${parsed.colorKey} / ${parsed.viewKey}`);
    }
    if (storagePaths.length > 20) {
      console.log(`... ${storagePaths.length - 20} more`);
    }
    return;
  }

  const { supabaseUrl, serviceRoleKey } = await loadEnv();
  if (!supabaseUrl || !serviceRoleKey) {
    throw new Error("NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY is missing.");
  }

  const supabase = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false },
  });

  await ensureBucket(supabase);

  const urls = {};
  for (const [index, filePath] of files.entries()) {
    const storagePath = toStoragePath(filePath);
    const publicUrl = await uploadFile(supabase, filePath, storagePath);
    setNestedUrl(urls, storagePath, publicUrl);
    console.log(`[${index + 1}/${files.length}] ${storagePath} -> ${publicUrl}`);
  }

  await fs.writeFile(OUTPUT_FILE, buildMockupUrlsFile(urls), "utf8");
  console.log(`\nGenerated ${path.relative(ROOT_DIR, OUTPUT_FILE)}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
